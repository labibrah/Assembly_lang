INCLUDE 'EMU8086.INC'
.MODEL SMALL         
.STACK 100H

.DATA
N DW 1   

.CODE
MOV AX, @DATA
MOV DS, AX

;for 7!

MOV CX, 7  
MOV AX, 1

FACT_7:
MUL CX      ;AX = 1*2*...*N
LOOP FACT_7 

CALL PRINT_NUM_UNS 
PRINTN

;for 8!

MOV CX, 8  
MOV AX, 1

FACT_8:
MUL CX      ;AX = 1*2*...*N
LOOP FACT_8 

CALL PRINT_NUM_UNS 
PRINTN
   


STOP_:
MOV AH,4CH
INT 21H


DEFINE_PRINT_NUM_UNS
DEFINE_PRINT_NUM
DEFINE_GET_STRING
DEFINE_PTHIS


END

