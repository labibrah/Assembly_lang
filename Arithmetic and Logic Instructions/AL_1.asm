INCLUDE 'EMU8086.INC'
.MODEL SMALL         
.STACK 100H

.DATA

.CODE      

MOV AX,@DATA
MOV DS,AX

CALL PTHIS 
DB  "Enter a number: ",0DH,0AH,0
CALL SCAN_NUM 
MOV AX,CX  
MOV BL,2
DIV BL 
TEST AL,1
JNZ ODD

EVEN:
PRINTN "EVEN"
JMP STOP_ 

ODD:
PRINTN "ODD"


STOP_:
MOV AH,4CH
INT 21H

DEFINE_GET_STRING
DEFINE_PTHIS
DEFINE_SCAN_NUM

END

