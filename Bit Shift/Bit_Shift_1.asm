INCLUDE 'EMU8086.INC'
.MODEL SMALL         
.STACK 100H

.DATA
RES DW 0
.CODE      

MOV AX,@DATA
MOV DS,AX

CALL PTHIS 
DB  "Enter 8 numbers: ",0DH,0AH,0    
MOV DX,8
XOR AX,AX
INPUT:
CMP DX,0
JE STOP_ 
DEC DX
CALL SCAN_NUM
PRINTN ""
ADD RES,CX
JMP INPUT

STOP_:
MOV AX,RES
SHR AX,3      
CALL PRINT_NUM
MOV AH,4CH
INT 21H

DEFINE_PRINT_NUM_UNS
DEFINE_PRINT_NUM
DEFINE_GET_STRING
DEFINE_PTHIS
DEFINE_SCAN_NUM

END

