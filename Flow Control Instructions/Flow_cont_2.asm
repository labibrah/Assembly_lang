.MODEL SMALL         
.STACK 100H

.DATA

.CODE      

MOV AX,@DATA
MOV DS,AX   

MOV AH,1

;TAKE INPUT
INT 21H

MOV CL, 2
DIV CL

CMP AH,  0
JE PRINT_E
JMP PRINT_O

;PRINT 'O'

PRINT_O:  
MOV AH,2
MOV DL, 0AH
INT 21H
MOV DL, 0DH 
INT 21H
MOV DL, 'O'
INT 21H  
JMP END

;PRINT 'E'

PRINT_E:  
MOV AH,2
MOV DL, 0AH
INT 21H
MOV DL, 0DH 
INT 21H
MOV DL, 'E'
INT 21H



END:
MOV AH, 4CH
INT 21H


END

