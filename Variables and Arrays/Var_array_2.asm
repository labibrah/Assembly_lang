.MODEL SMALL         
.STACK 100H

.DATA
ARR1 DB 5 DUP (?), '$'
ARR2 DB 5 DUP (?), '$' 

.CODE      

MOV AX,@DATA
MOV DS,AX   

MOV AH,1

;TAKE INPUT
INT 21H
MOV ARR1[0], AL  

INT 21H
MOV ARR1[1], AL

INT 21H
MOV ARR1[2], AL

INT 21H
MOV ARR1[3], AL

INT 21H
MOV ARR1[4], AL 

;COPY ELEMENTS

LEA SI, ARR2

MOV AL, ARR1[0]
MOV [SI],  AL 

MOV AL, ARR1[1]
MOV [SI+1],  AL

MOV AL, ARR1[2]
MOV [SI+2],  AL

MOV AL, ARR1[3]
MOV [SI+3] , AL

MOV AL, ARR1[4]
MOV [SI+4],  AL   

;PRINT ON NEW LINE    


MOV AH,2
MOV DL, 0AH
INT 21H
MOV DL, 0DH 
INT 21H


;PRINT SECOND ARRAY


LEA DX, ARR2
MOV AH,9
INT 21H


MOV AH, 4CH
INT 21H


END

